function searchWord(word, string) {
  let arrayStrings = string.split(' ');

  for (let idx = 0; idx < arrayStrings.length; idx++) {
    if (arrayStrings[idx].toLowerCase() === word.toLowerCase()) {
      arrayStrings[idx] = highlight(word);
    }
  }

  return arrayStrings.join(' ');
}

function highlight(word) {
  return `**${word.toUpperCase()}**`;
}

// or

function searchWord_(word, text) {
  let regex = new RegExp(word, "gi");
  return text.replace(regex, `**${word.toUpperCase()}**`);
}

